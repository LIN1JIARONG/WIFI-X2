/*******************************************************************************
* 函数名: crc16_ccitt
* 功  能: crc校验
* 输  入: *buf：校验数组  len：校验数组长度
* 输  出: none
* 返回值: crc：校验结果
* 注  意:  ＾（异或运算）
*******************************************************************************/
unsigned short crc16_ccitt(const unsigned char *buf, int len)
{
	register int counter;
	register unsigned char crc_H8=0;
	register unsigned short crc = 0;

	for( counter = 0; counter < len; counter++)
	{
		crc_H8 = (unsigned char)(crc>>8);
	    crc = crc<<8;

		crc = crc ^ crc16tab[(crc_H8 ^ *buf)&0x00FF];
		buf++;
	}
	return crc;
}


int GetCRC16(unsigned char* pchMsg, int wDataLen)
{
        char chCRCHi = 0xFF; 	// 高CRC字节初始化
        char chCRCLo = 0xFF; 	// 低CRC字节初始化
        int wIndex;         	// CRC循环中的索引

        while (wDataLen--)
        {
                // 计算CRC
                wIndex = chCRCLo ^ *pchMsg++ ;
                chCRCLo = chCRCHi ^ chCRCHTalbe[wIndex];
                chCRCHi = chCRCLTalbe[wIndex] ;
        }

        return ((chCRCHi << 8) | chCRCLo) ;
}

/*******************************************************************************
* 函数名: crc16_ccitt
* 功  能: crc校验
* 输  入: *Buff：校验数组  nSize：校验数组长度
* 输  出: none
* 返回值: crc：校验结果
* 注  意:  ＾（异或运算）
*******************************************************************************/
unsigned short int  Modbus_Caluation_CRC16(unsigned char *Buff, unsigned int nSize)
{
    unsigned short int m_Crc = 0;
    unsigned short int i = 0,j = 0;
    unsigned short int m_InitCrc = 0xFFFF;

    for(i=0; i<nSize; i++)
    {
		m_InitCrc ^= Buff[i];
        for(j=0; j<8; j++)
        {
            m_Crc = m_InitCrc;
            m_InitCrc >>= 1;
            if(m_Crc&0x0001)
            {
                m_InitCrc ^= 0xa001;
            }
        }
    }
    return m_InitCrc;
}


