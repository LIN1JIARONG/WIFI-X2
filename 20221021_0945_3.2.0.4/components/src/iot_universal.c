/******************************************************************************
 * FunctionName : IOT_itoh
 * Description  : ESP 十进制转十六进制(无符号)
 * Parameters   : char *hex    : 存放转化的16进止数
 *                u32 dn       : 被转化的十进制数
 *                u8 length    : 转化的数据长度
 * Returns      : none
 * Notice       : none
*******************************************************************************/
void IOT_itoh(unsigned char *hex, unsigned int dn, unsigned char length)
{
    unsigned char i = length;

    if(i == 0)return;

    for(; i>0; i--)
    {
       hex[i-1] = dn % 256;
       dn >>= 8;
    }
}

/******************************************************************************
 * FunctionName : IOT_htoi
 * Description  : ESP 十六进制转十进制(无符号)
 * Parameters   : char *hex    : 被转化的16进止数
 *                u8 length    : 转化的数据长度, 长度等于1时，该函数无意义
 * Returns      : dn           : 转化后的值
 * Notice       : 举例：0x10 0x00 --> 4096
*******************************************************************************/
unsigned int IOT_htoi(unsigned char *hex, unsigned char length)
{
    unsigned char i = length,j = 0;
    unsigned char uchighbyte = 0, uclowbyte = 0;
    unsigned int dn = 0;

    if(i == 0)return 0;

    for(; i>0; i--, j+=2)
    {
    	uchighbyte = (hex[i-1]>>4)&0x0F;
    	uclowbyte = hex[i-1]&0x0F;

    	dn += uchighbyte*pow(16,j+1) + uclowbyte*pow(16,j);
    }

    return dn;
}

/******************************************************************************
 * FunctionName : IOT_ctoi
 * Description  : 字符转换为十进制(可重入函数)
 * Parameters   : Number： 字符个数
 *                Chr： 将要转化的字符串
 * Returns      : usiValue
 * Notice       : 1：pow(x,y)---> x 的 y 次方  头文件<math.h>
                     举例："100" --> 100
*******************************************************************************/
unsigned int IOT_ctoi(unsigned char Number,unsigned char *Chr)
{
	  unsigned short int usiValue = 0;
    unsigned char i = 0;
	  unsigned char ucchr = 0;

	  for(;i<Number;i++)
	  {
			if((Chr[i]>='0')&&(Chr[i]<='9'))ucchr=Chr[i]-'0';          // 字符 ‘0’-‘9’

			usiValue += ucchr*pow(10,Number-i-1);
	  }

	  return usiValue;
}


/******************************************************************************
 * FunctionName : IOT_ESP_strstr
 * Description  : esp8266 数据比较
 * Parameters   : char *FoundBuf       : 被查找数组(源字符串数组)
 *                const char *FindStr  : 要查找字符串
 *                u16 sumnum           : 被查找字符串总长度
 *                u8 findnum           : 查找字符长度
 * Returns      : 0-无查询字符串 / !0-有查询字符串
*******************************************************************************/
const char* IOT_ESP_strstr(const unsigned char *FoundBuf, const unsigned char *FindStr,uint16_t sumnum,uint8_t findnum)
{
	uint8_t i = 0;
	const unsigned char *FoundBufPiont;     // 存放被查找数组
	const unsigned char *FindStrPoint;      // 存放要查找字符串

	if(sumnum < findnum)return 0;  // 要查询字符数笔总字符数长，默认查询失败

	while(sumnum--)
	{
		i = 0;
		FoundBufPiont = FoundBuf;  // 被查找数组起始地址不断更新
		FindStrPoint = FindStr;    // 保持不变

		while(*FoundBufPiont == *FindStrPoint)
		{
			i++;
			if(i == findnum)return (const char*)FoundBuf;// 要查找字符串地址值为 0x00，查询结束

			FoundBufPiont++;
			FindStrPoint++;
		}

		FoundBuf += 1;                      // 每循环一次被查找数组起始地址后移一位
	}
	return 0;
}
